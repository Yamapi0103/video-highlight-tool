<template>
  <div class="video-controls bg-gray-900 bg-opacity-90 p-3 rounded-b-lg">
    <!-- 進度條 -->
    <VideoProgressBar
      :current-time="currentTime"
      :duration="duration"
      :buffered="buffered"
      :video-element="videoElement"
      :highlight-segments="highlightSegments"
      @seek="$emit('seek', $event)"
      class="mb-3"
    />
    
    <!-- 控制按鈕區 -->
    <div class="flex items-center justify-between">
      <div class="flex items-center gap-3">
        <!-- 播放/暫停按鈕 -->
        <button
          @click="$emit('toggle-play')"
          class="text-white hover:text-blue-400 transition-colors"
        >
          <svg v-if="!isPlaying" class="w-6 h-6" fill="currentColor" viewBox="0 0 24 24">
            <path d="M8 5v14l11-7z" />
          </svg>
          <svg v-else class="w-6 h-6" fill="currentColor" viewBox="0 0 24 24">
            <path d="M6 4h4v16H6V4zm8 0h4v16h-4V4z" />
          </svg>
        </button>
        
        <!-- 時間顯示 -->
        <div class="text-white text-sm">
          {{ formatTime(currentTime) }} / {{ formatTime(duration) }}
        </div>
      </div>
      
      <div class="flex items-center gap-3">
        <!-- 音量控制 -->
        <div class="flex items-center gap-2">
          <button
            @click="toggleMute"
            class="text-white hover:text-blue-400 transition-colors"
          >
            <svg v-if="!isMuted && volume > 0.5" class="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
              <path d="M3 9v6h4l5 5V4L7 9H3zm13.5 3c0-1.77-1.02-3.29-2.5-4.03v8.05c1.48-.73 2.5-2.25 2.5-4.02zM14 3.23v2.06c2.89.86 5 3.54 5 6.71s-2.11 5.85-5 6.71v2.06c4.01-.91 7-4.49 7-8.77s-2.99-7.86-7-8.77z" />
            </svg>
            <svg v-else-if="!isMuted && volume > 0" class="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
              <path d="M18.5 12c0-1.77-1.02-3.29-2.5-4.03v8.05c1.48-.73 2.5-2.25 2.5-4.02zM5 9v6h4l5 5V4L9 9H5z" />
            </svg>
            <svg v-else class="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
              <path d="M16.5 12c0-1.77-1.02-3.29-2.5-4.03v2.21l2.45 2.45c.03-.2.05-.41.05-.63zm2.5 0c0 .94-.2 1.82-.54 2.64l1.51 1.51C20.63 14.91 21 13.5 21 12c0-4.28-2.99-7.86-7-8.77v2.06c2.89.86 5 3.54 5 6.71zM4.27 3L3 4.27 7.73 9H3v6h4l5 5v-6.73l4.25 4.25c-.67.52-1.42.93-2.25 1.18v2.06c1.38-.31 2.63-.95 3.69-1.81L19.73 21 21 19.73l-9-9L4.27 3zM12 4L9.91 6.09 12 8.18V4z" />
            </svg>
          </button>
          
          <input
            type="range"
            min="0"
            max="1"
            step="0.1"
            :value="volume"
            @input="$emit('volume-change', parseFloat(($event.target as HTMLInputElement).value))"
            class="w-20 accent-blue-500"
          />
        </div>
        
        <!-- 全螢幕按鈕 -->
        <button
          @click="$emit('toggle-fullscreen')"
          class="text-white hover:text-blue-400 transition-colors"
        >
          <svg class="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
            <path d="M7 14H5v5h5v-2H7v-3zm-2-4h2V7h3V5H5v5zm12 7h-3v2h5v-5h-2v3zM14 5v2h3v3h2V5h-5z" />
          </svg>
        </button>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { ref, computed } from 'vue'
import VideoProgressBar from './VideoProgressBar.vue'

const props = defineProps<{
  currentTime: number
  duration: number
  isPlaying: boolean
  volume: number
  isMuted: boolean
  buffered?: TimeRanges
  videoElement?: HTMLVideoElement
  highlightSegments?: Array<{
    id: string
    startTime: number
    endTime: number
  }>
}>()

const emit = defineEmits<{
  'toggle-play': []
  'seek': [time: number]
  'volume-change': [volume: number]
  'toggle-fullscreen': []
}>()

const toggleMute = () => {
  if (props.isMuted) {
    emit('volume-change', props.volume || 0.5)
  } else {
    emit('volume-change', 0)
  }
}

const formatTime = (seconds: number): string => {
  if (!seconds || isNaN(seconds)) return '0:00'
  const mins = Math.floor(seconds / 60)
  const secs = Math.floor(seconds % 60)
  return `${mins}:${secs.toString().padStart(2, '0')}`
}
</script>